function createItemSlug(itemName) {
  if (!itemName) return '';
  return itemName.toLowerCase()
    .replace(/\s+/g, '-')
    .replace(/'/g, '')
    .replace(/[^\w-]/g, '');
}

function getTypeImageUrl(type) {
  return TYPE_IMAGES[type?.toLowerCase()] || TYPE_IMAGES['default'];
}

export function getItemImage(itemName, itemType = 'sword') {
  const slug = createItemSlug(itemName);
  const originalImage = TIBIA_ORIGINAL_IMAGES[slug];
  const fallbackUrl = getTypeImageUrl(itemType);

  if (originalImage) {
    return `<img src="${originalImage}"
                 alt="${itemName}"
                 class="tibia-item-img"
                 loading="lazy"
                 onerror="this.src='${fallbackUrl}'" />`;
  }

  return `<img src="${fallbackUrl}" alt="${itemType}" class="tibia-item-img" loading="lazy" />`;
}

export function getSlotIconHtml(slotId) {
  const url = TYPE_IMAGES[slotId] || TYPE_IMAGES['default'];
  return `<img src="${url}" alt="${slotId}" class="tibia-slot-icon" loading="lazy" onerror="this.style.display='none'" />`;
}

export function getStatIconHtml(stat) {
  const url = STAT_IMAGES[stat?.toLowerCase()] || TYPE_IMAGES['default'];
  return `<img src="${url}" alt="${stat}" class="tibia-stat-icon" loading="lazy" onerror="this.style.display='none'" />`;
}

export function getRarityIcon(rarity) {
  const colors = {
    'common':   '#9ca3af',
    'uncommon': '#10b981',
    'rare':     '#3b82f6',
    'epic':     '#a855f7',
    'legendary':'#f59e0b'
  };
  const color = colors[rarity?.toLowerCase()] || '#9ca3af';
  return `<span style="display:inline-block;width:12px;height:12px;border-radius:50%;background:${color};flex-shrink:0;vertical-align:middle;"></span>`;
}

export function getClassIcon(className) {
  const url = CLASS_IMAGES[className?.toLowerCase()] || CLASS_IMAGES['knight'];
  const fallback = TYPE_IMAGES['sword'];
  return `<img src="${url}" alt="${className}" class="tibia-class-icon" loading="lazy" onerror="this.src='${fallback}'" />`;
}

export function getIconForItemType(type) {
  return getSlotIconHtml(type);
}

export function getItemIcon(itemName, itemType) {
  return getItemImage(itemName, itemType);
}

export function createIconElement(iconData, alt = '', className = 'item-icon') {
  if (typeof iconData === 'string' && iconData.includes('<img')) {
    return iconData;
  }
  const url = TYPE_IMAGES['default'];
  return `<img src="${url}" alt="${alt}" class="${className}" style="width:1.5rem;height:1.5rem;object-fit:contain;" />`;
}

